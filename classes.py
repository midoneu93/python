class calculator :

    def addition(x, y):
        added = x + y
        print(added)

    def subtraction(x, y):
        sub = x - y
        print(sub)

    def multiplication(x, y):
        mult = x * y
        print(mult)

    def division(x, y):
        div = x / y
        print(div)

calculator.addition(10, 5)
calculator.subtraction(10, 5)
calculator.multiplication(10, 5)
calculator.division(10, 5)